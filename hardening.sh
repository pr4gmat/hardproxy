# === Цвета ===
VIEW_RESET="\e[0m"
VIEW_BOLD="\e[1m"
VIEW_RED="\e[31m"
VIEW_GREEN="\e[32m"
VIEW_YELLOW="\e[33m"
VIEW_BLUE="\e[34m"
VIEW_MAGENTA="\e[35m"
VIEW_CYAN="\e[36m"
VIEW_WHITE="\e[97m"

# === Логотип ===
view_rainbow_logo() {
    local logo_lines=(
        " __   __  _______  ______    ______   _______  __    _  ___   __    _  _______        _______  __   __ "
        "|  | |  ||   _   ||    _ |  |      | |       ||  |  | ||   | |  |  | ||       |      |       ||  | |  |"
        "|  |_|  ||  |_|  ||   | ||  |  _    ||    ___||   |_| ||   | |   |_| ||    ___|      |  _____||  |_|  |"
        "|       ||       ||   |_||_ | | |   ||   |___ |       ||   | |       ||   | __       | |_____ |       |"
        "|       ||       ||    __  || |_|   ||    ___||  _    ||   | |  _    ||   ||  | ___  |_____  ||       |"
        "|   _   ||   _   ||   |  | ||       ||   |___ | | |   ||   | | | |   ||   |_| ||   |  _____| ||   _   |"
        "|__| |__||__| |__||___|  |_||______| |_______||_|  |__||___| |_|  |__||_______||___| |_______||__| |__|"
        ""
        "Version: v1.0"
    )

    local colors=("$VIEW_RED" "$VIEW_YELLOW" "$VIEW_GREEN" "$VIEW_CYAN" "$VIEW_BLUE" "$VIEW_MAGENTA")
    local color_count=${#colors[@]}
    local i=0

    echo -e "${VIEW_BOLD}"
    for line in "${logo_lines[@]}"; do
        local color=${colors[$((i % color_count))]}
        echo -e "${color}${line}${VIEW_RESET}"
        ((i++))
    done
    echo
}

# === Баннер секции ===
view_banner() {
    local title="$1"
    echo -e "${VIEW_BOLD}${VIEW_CYAN}================ [ $title ] ================${VIEW_RESET}"
}

# === Логирование ===
view_log() {
    local type="$1"
    local msg="$2"
    local color="$VIEW_WHITE"

    case "$type" in
        INFO) color="$VIEW_CYAN" ;;
        OK) color="$VIEW_GREEN" ;;
        WARN) color="$VIEW_YELLOW" ;;
        ERROR) color="$VIEW_RED" ;;
    esac

    echo -e "${VIEW_BOLD}${color}[$type]${VIEW_RESET} $msg"
}

